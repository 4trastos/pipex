#include "../incl/pipex.h"

void	ft_error_msg(char *msg, char **str)
{
	int	i;

	i = 0;
	if (str)
		free(str);
	while(msg[i] != '\0')
	{
		write(1, &msg[i], 1);
		i++;
	}
	write(1, "\n", 1);
	exit (1);
}
